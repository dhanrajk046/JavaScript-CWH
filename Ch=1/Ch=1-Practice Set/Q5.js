// Javascript program to create a word meaning dictionary

const dictionary = {
    Apple: "A sweet and edible fruit.",
    Book: "A collection of written or printed pages",
    Computer: "An electronic device used for processsing data.",
    Honest: "Truthful and sincere.",
    Courage: "The ability to face fear or difficulties."
};

// Display the dictionary
console.log(dictionary);

//Display meanings of individual words

console.log("Apple:", dictionary.Apple);
console.log("Book:", dictionary.Book);
console.log("Computer:", dictionary.Computer);
console.log("Honest:", dictionary.Honest);
console.log("Courage:", dictionary.Courage);
